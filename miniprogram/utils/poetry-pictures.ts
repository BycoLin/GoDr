/** 仅返回有专属映射的 emoji（看图猜篇专用） */
export function distinctPoetryEmoji(item: PoetryItem): string | null {
  return POETRY_EMOJI[item.title] || null;
}

export function poetryEmojiFor(item: PoetryItem): string | null {
  return distinctPoetryEmoji(item);
}

export function hasPoetryPicture(item: PoetryItem): boolean {
  return distinctPoetryEmoji(item) !== null;
}
