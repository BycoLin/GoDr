function normalizeWord(word: string): string {
  return word.toLowerCase().trim().split(/\s+/)[0];
}

export function emojiForEnglishItem(item: EnglishItem): string | null {
  const w = normalizeWord(item.word);
  if (WORD_EMOJI[w]) return WORD_EMOJI[w];
  if (w === 'orange' && item.category === 'color') return 'рҹҹ ';
  if (item.category && CATEGORY_EMOJI[item.category]) {
    return CATEGORY_EMOJI[item.category];
  }
  return null;
}

export function hasEnglishPicture(item: EnglishItem): boolean {
  return emojiForEnglishItem(item) !== null;
}
